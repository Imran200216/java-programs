
public class arithmetic_operator {
    public static void main(String[] args) {

        /*
         * Arithmetic operators:
         * 1. Addition
         * 2. Subtraction
         * 3. Multiplication
         * 4. Dividsion
         * 5. Modulus
         * 6. Increment
         * 7. Decrement
         */

        int a = 30;
        int b = 10;

        /// Addition
        int additionOperation = a + b;
        System.out.println(additionOperation);

        /// Subtraction
        int subtractionOperation = a - b;
        System.out.println(subtractionOperation);

        /// Multiplication
        int multiplicatonOperation = a * b;
        System.out.println(multiplicatonOperation);

        /// Division
        int divisionOperation = a / b;
        System.out.println(divisionOperation);

        /// Modulus
        int modulusOperation = a % b;
        System.out.println(modulusOperation);

        /// increment (++)
        int incrementOperation = a++;
        System.out.println(incrementOperation);

        /// decrement (--)
        int decrementOperation = b--;
        System.out.println(decrementOperation);

    }
}
