class number_data_types {

    public static void main(String[] args) {

        /*
         * Types of data types in number
         * 1. Byte - instead of int we can use byte it stores whole numbers -128 to 127
         * 2. Short - it stores the whole numbers from -32768 to 32767
         * 3. Int - it stores the whole numbers
         * 4. Long
         * 5. Float
         * 6. Double
         */

        byte myBytes = 100;
        System.out.println(myBytes);

        short myShort = 5000;
        System.out.println(myShort);

        long myLong = 2000;
        System.out.println(myLong);

        float myFloat = 2.45f;
        System.out.println(myFloat);

        double myDouble = 2.00d;
        System.out.println(myDouble);

    }

}