class data_types {
    public static void main(String[] args) {

        /// integer
        int a = 20;
        System.out.println(a);

        /// float
        float b = 30.4f;
        System.out.println(b);

        /// double
        double c = 40;
        System.out.println(c);

        /// string
        String d = "Imran";
        System.out.println(d);

        /// character
        char e = 'e';
        System.out.println(e);

        /// boolean
        boolean isRead = true;
        System.out.println(isRead);

    }
}