public class assignment_operator {

    public static void main(String[] args) {
        /*
         * Assignment Operator
         * 1. Add equal to
         * 2. Subtract equal to
         * 3. equal to
         * 4. Multiplication equal to
         * 5. Division equal to
         * 6. Modulus equal to
         * 7. And operator and equal to
         * 8. Or operator and equal to
         * 9. Not operator and equal to
         * 10. Xor operator and equal to
         * 11. Right Shift operation
         * 12. Left Shift operation
         */

        /// equal to
        int a = 20;

        /// add equal to
        a += 5;
        System.out.println(a);

        /// subtract equal to
        a -= 10;
        System.out.println(a);

        /// multiplication equal to
        a *= 10;
        System.out.println(a);

        /// division equal to
        a /= 10;
        System.out.println(a);

        /// modulus equal to
        a %= 10;
        System.out.println(a);

        /// AND equal to
        a &= 10;
        System.out.println(a);

        /// OR equal to
        a |= 10;
        System.out.println(a);

        /// XOR equal to
        a ^= 10;
        System.out.println(a);

        /// right shift
        a >>= 10;
        System.out.println(a);

        /// left shift
        a <<= 10;
        System.out.println(a);

    }

}
